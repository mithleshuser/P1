public class RvkCenter {
	private int centerCode;
	private String pracharSahayakName;
	private String sebaSahayakName;
	private String kendraSahayakName;
	public int getCenterCode() {
		return centerCode;
	}
	public void setCenterCode(int centerCode) {
		this.centerCode = centerCode;
	}
	public String getPracharSahayakName() {
		return pracharSahayakName;
	}
	public void setPracharSahayakName(String pracharSahayakName) {
		this.pracharSahayakName = pracharSahayakName;
	}
	public String getSebaSahayakName() {
		return sebaSahayakName;
	}
	public void setSebaSahayakName(String sebaSahayakName) {
		this.sebaSahayakName = sebaSahayakName;
	}
	public String getKendraSahayakName() {
		return kendraSahayakName;
	}
	public void setKendraSahayakName(String kendraSahayakName) {
		this.kendraSahayakName = kendraSahayakName;
	}
	
}
	